//11 WAP to illustrate the use of unary postfix increment and decrement operators.
#include<stdio.h>
int main(){
int num = 3;
printf("\n The number is %d", num);
printf("\n The number after increment is %d", num++);
printf("\n The number is %d", num);
printf("\n The number after decrement is %d", num--);
printf("\n The new number is %d", num);
return 0;
}